SELECT HOTEN FROM CAUTHU
JOIN THAMGIA ON CAUTHU.MACT = THAMGIA.MACT
JOIN TRANDAU ON THAMGIA.MATD = TRANDAU.MATRAN
GROUP BY HOTEN HAVING SUM(SOTRAI) >= 2

